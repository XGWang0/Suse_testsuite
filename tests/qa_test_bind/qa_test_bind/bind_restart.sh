set -eu

WORKPATH="/usr/share/qa/qa_test_bind"
. $WORKPATH/bind.rc


setup() {
	backup_config
	install_config "forwarder"
}

test_() {
	[ ! -e $PID_FILE ] && bctl_ww start

	OLDPID=`cat $PID_FILE`

	bctl_ww restart

	if [ -e $PID_FILE ]; then
		NEWPID=`cat $PID_FILE`

		if [ $OLDPID != $NEWPID ]; then
			echo "PASSED: bind - Restart daemon"
			return $RES_OK
		fi
	fi

	echo "FAILED: bind - Restart daemon"
	return $RES_FAIL
}

main $@
