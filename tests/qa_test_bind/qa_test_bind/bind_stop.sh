set -eu

WORKPATH="/usr/share/qa/qa_test_bind"
. $WORKPATH/bind.rc

setup() {
	backup_config
	install_config "forwarder"

	bctl_ww restart

	if [ ! -e $PID_FILE ]; then
		echo "FAILED: bind - Stop daemon (could not start to stop)"
		return $RES_FAIL_SETUP
	fi
}

test_() {
	PID=`cat $PID_FILE`
	bctl_ww stop

	ps -p $PID > /dev/null && {
		echo "FAILED: bind - Stop daemon (didn't stop)"
		return $RES_FAIL
	}

	echo "PASSED: bind - Stop daemon"
	return $RES_OK
}

main $@
